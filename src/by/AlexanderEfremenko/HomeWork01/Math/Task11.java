public class Task11 {
	public static void main(String[] args) {
		double a;
		double b;
		double s;
		double p;
		double g;

		a = 3;
		b = 4;

		s = (a * b) / 2;
		g = Math.sqrt(Math.pow(a, 2) + Math.pow(b, 2));
		p = a + b + g;

		System.out.println("Result S = " + s);
		System.out.println("Result P = " + p);

	}

}
